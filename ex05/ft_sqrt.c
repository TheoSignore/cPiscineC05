int	ft_sqrt(int nb)
{
	int i;

	if (nb == 1)
		return (nb);
	if (nb > 2147483641)
		return (0);
	i = 0;
	while (i++ < nb)
	{
		if (i > 593968971)
			return (0);
		if ((i * i) == nb)
			return (i);
	}
	return (0);
}

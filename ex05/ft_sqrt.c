int	ft_sqrt(int nb)
{
	int i;
	
	if(nb == 1)
		return (nb);
	i = nb > 4 ? nb / 2 : nb;
	while (i-- > 0)
	{
		if ((i * i) == nb)
			return (i);
	}
	return (0);
}
